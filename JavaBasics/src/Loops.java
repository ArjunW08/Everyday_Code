public class Loops {
    /* 
     * For Loop
     * While Loops
     * Do-While Loops
     * Nested Loops
     * Break and Continue Statement can be used with loops.
    */

    public static void main(Strings[] args) {
        int i = 0, j = 0;
        while(i < 5){
            System.out.println("Number : " + i);
            i++;
        }

        do{
            System.out.println("Starting");
            j++;
        }while(j<2);
    }
}
